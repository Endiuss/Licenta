package BotBinance;

public class BinanceException extends Exception {

	private static final long serialVersionUID = 1L;

	
		  public BinanceException() { super(); }
		  public BinanceException(String message) { super(message); }
		  public BinanceException(String message, Throwable cause) { super(message, cause); }
		  public BinanceException(Throwable cause) { super(cause); }
		
}
